program=$1
path=$(pwd)
mkdir ${path}/${program}libso/
ldd ${path}/${program} | awk '{print $3}' > ${path}/"program.txt"
linenum=$(wc -l ${path}/"program.txt" | awk '{print $1}')
echo $linenum
i=1
for((i;i <= $linenum;i++))
do
line=$(sed -n ${i}'p' ${path}/program.txt)
echo $line
cp $line ${path}/${program}libso/ -a

done
